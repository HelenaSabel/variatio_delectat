            <h2><span class="pt">Metodologia</span><span class="en">Methodology</span><span
                    class="gl">Metodoloxía</span></h2>
            <p><span class="pt">A nossa proposta para sistematizar o estudo da variação linguística
                    na lírica profana galego-portuguesa, consiste numa edição digital sinóptica. A
                    grandes traços, fundamenta-se na colação de cada uma das leituras de todos os
                    testemunhos conservados, contrastando cada forma e signo automaticamente. O
                    suporte da edição é a linguagem de marcas XML e esta marcação faz-se seguindo as
                    normas estabelecidas pela Text Encoding Initiative (TEI). Concretamente,
                    utilizamos o método de segmentação em paralelo, pois permite a codificação de
                    várias versões dum mesmo texto num único documento.</span></p>
            <p><span class="pt">Esta anotação facilita a identificação de cada variante para
                    posteriormente ser categorizada. Para marcá-las e defini-las, utilizamos as
                    chamadas “estruturas de traços” (Feature Structures). Assim, criamos uma
                    livraria de traços mínimos com o fim de categorizar as variantes atendendo à sua
                    tipologia (erros de cópia, variantes de língua, lições equipolentes ou variantes
                    gráficas). Evidentemente, os valores que conformam o conteúdo de cada estrutura
                    de traços que define um fenómeno linguístico são muito mais complexos (em
                    quantidade e concreção) que aqueles que utilizamos para definir as outras
                    tipologias de variação.</span></p>
            <p><span class="pt">A flexibilidade na hora de combinar os traços mínimos e a
                    especificidade com que são definidos permite-nos extrair com muita precisão os
                    fenómenos de variação linguística. Para extrair e manipular eficazmente a
                    complexa informação contida em esta marcação utilizamos um gestor de bases de
                    dados não-SQL e a linguagem de consulta XQuery.</span></p>
            <p><span class="gl">En primeiro lugar, procedeuse a elixir unha parte do corpus presente
                    nos tres testemuños arriba mencionados, é dicir, os cancioneiros <em>A</em>,
                        <em>B</em> e <em>V</em>. Coa fin de poder organizar a información procedente
                    da comparación sistemática dos códices, requírese dunha transcrición dos
                    manuscritos con criterios actualizados, así como dunha <em>collatio</em>
                    rigorosa dos textos. A través de diferentes etapas, marcáronse as cantigas para
                    introducir información descritiva e analítica das mesmas. A metodoloxía de
                    edición proposta segue o modelo dispositivo que caracteriza a denominada edición
                    sinóptica e utiliza como soporte a linguaxe XML-TEI. Este formato permite
                    habilitar a sistematización da variación textual, xa que, por unha parte,
                    posibilita o contraste automático entre as diferentes leccións presentes nos
                    códices e, por outra, facilita a recuperación inmediata de todos os datos
                    necesarios para examinar a variación lingüística e os aspectos que a ela se
                    vinculan –como os relativos á tradición manuscrita e á súa
                    estratificación–.</span>
                <span class="en">I selected a portion of the corpus that is preserved in the three
                    main witnesses of the Galician-Portuguese lyric, that is, the above mentioned
                        <em>A</em>, <em>B</em> and <em>V</em>. In order to organize the information
                    that results from systematically comparing these codices, it is first necessary
                    to collate the witnesses rigorously. To support this collation, I implemented a
                    computational processing pipeline that added successive layers of markup to the
                        <em>cantigas</em> to introduce different types of descriptive and analytic
                    information. I implemented this markup using XML technologies, which made it
                    possible then to systematize my study of textual variation, since the markup
                    enabled the automatic identification and categorization of different readings in
                    the codices. The XML-based architecture also supports, in a way that is
                    relatively easy to develop and maintain, the retrieval of data required to
                    examine specific types of linguistic variation and the derived consequences of
                    that variation, such as those related to the manuscript tradition and its
                    stratification.</span></p>
            <p><span class="gl">No que se refire ao soporte tecnolóxico, non existen límites no que
                    respecta á información que quen edita pode engadir a un texto. Porén, a
                    codificación dos múltiples e heteroxéneos datos que poderían ser integrados
                    nunha edición dixital require dunha conceptualización previa que teña en conta
                    as necesidades informativas que precisan ser representadas, así como as propias
                    restricións do modelo XML (unha xerarquía ordenada que non permite
                    solapamentos). No noso caso, precisábanse introducir múltiples capas de
                    información que permitisen definir cun gran nivel de detalle os fenómenos de
                    variación. Ante esta circunstancia, os métodos máis habituais de marcado (a
                    marcación na liña) resultaban ineficientes. Por este motivo, tívose que pór en
                    práctica un sistema de codificación externa que permitise aplicar unha anotación
                    editorial conformada por camadas de datos múltiples e xerarquizadas. A nosa
                    proposta é compatíbel coas recomendacións do consorcio internacional <em>Text
                        Encoding Initiative</em> (TEI), dedicado a manter as directrices para a
                    codificación de textos (<em>TEI Guidelines</em>). </span><span class="en">With
                    regard to the editing technologies, there are no limits to the information that
                    can be added to a digital text. For that reason, encoding the multiple and
                    heterogeneous data that could be embedded into a digital edition requires a
                    prior conceptualization that takes into account the informational needs that
                    must be made explicit, as well as any accommodations required to overcome
                    restrictions of the XML model (an ordered hierarchy which does not allow
                    overlap). In my case, I needed to introduce multiple layers of information that
                    allowed me to define the phenomena of variation with a great level of detail.
                    That is why the most common methods of encoding, such as inline markup, were
                    inefficient, and it became necessary to implement a stand-off annotation method
                    that allowed me to apply an editorial annotation consisting of multiple
                    hierarchies. Digital philological inquiry must negotiate a balance between
                    reliance on well-known, well-documented, and widely used standards, on the one
                    hand, and highly individual research goals that may not have been addressed
                    previously by other scholars, on the other. My research observes that balance,
                    so that my original proposal for modeling highly complex linguistic and textual
                    phenomena using stand-off markup is fully compatible with the recommendations of
                    the Text Encoding Initiative (TEI). The TEI is an international consortium
                    dedicated to maintaining a protocol for editing and marking up texts, known as
                    the <em>TEI Guidelines</em>.</span></p>
            <p><span class="gl">As directrices de TEI non teñen un forte carácter prescritivo, polo
                    que presentan unha grande flexibilidade, permitindo que se poidan marcar os
                    mesmos fenómenos de diferentes maneiras segundo a interpretación e o obxectivo
                    de quen codifica. Na hora de marcar as leccións dos testemuños, tamén conviven
                    diferentes formas de identificalas, así como diversos métodos para vincular a
                    información do aparato co texto. Dos métodos dispoñíbeis, utilizamos o chamado
                    método de segmentación en paralelo. Este faculta para diferenciar perfectamente
                    o texto común das variantes, facendo uso dunha serie de elementos que recoñecen
                    cada unidade de variación, isto é, márcase cada <em>locus</em> no texto en que
                    polo menos concorren dúas leccións.</span><span class="en">The TEI
                        <em>Guidelines</em> are not meant to be prescriptive rules. They are
                    flexible, and is is possible to mark up the same material phenomenon in
                    different ways (including custom ways created for a specific project) according
                    to the interpretation and the objectives of the encoder. When annotating the
                    different readings of the witnesses, different ways of identifying them also
                    coexist, as well as various methods for linking the information of the critical
                    apparatus with the text. Of the alternatives supported by the TEI, I use the
                    so-called “parallel segmentation” method because it is especially well suited to
                    distinguishing common text from text that varies across witnesses. Specifically,
                    the TEI uses a set of elements and attributes to identify each unit of
                    variation, that is, each <em>locus</em> where at least two readings
                    compete.</span></p>
            <p><span class="en">Once transcriptions of the witnesses had been prepared according to
                    editorial criteria that preserve their significant orthographic and linguistic
                    particularities, I developed a program in XSLT, an XML-based programming
                    language, to perform the computational collation of the witnesses. This program
                    generates an output in TEI that, insofar as it is compliant with the parallel
                    segmentation method, differentiates the readings that diverge from one
                    manuscript to another. This model also makes it possible to explore different
                    graphic visualizations of orthographic and linguistic phenomena that greatly
                    ease the comparison of variants.</span><span class="gl">Unha vez que se
                    ultimaron as transcricións dos testemuños con criterios de edición que
                    preservasen as particularidades grafemáticas e lingüísticas de cada un deles,
                    elaboramos un programa en XSLT para facer a colación computacional dos mesmos.
                    Este programa xera unha saída en TEI que, seguindo o método de codificación en
                    paralelo, diferencia, a través da marcación, as leccións que diverxen dun
                    manuscrito a outro. Este modelo permite crear, con facilidade, diferentes
                    visualizacións que posibilitan ver as variantes dunha maneira
                inmediata.</span></p>
            <p><span class="gl">O seguinte paso da investigación consistiu na descrición e
                    clasificación de cada unha das unidades de variación. Para poder conceptualizar
                    a fenomenoloxía da variación, decidimos elaborar unha taxonomía de variantes.
                    Para o seu modelado, fixemos uso do módulo de TEI relativo ás estruturas de
                    trazos. Unha estrutura de trazos (<em>feature structure</em>) consiste nun
                    conxunto de pares <em>atributo:valor</em>, no que os valores poden ser atómicos
                    ou outras estruturas de trazos (aniñadas). É dicir, un elemento é descrito a
                    partir dunha serie de propiedades ás que se lles dá un valor. No entanto, este
                    valor pode estar conformado, á súa vez, por diferentes propiedades cos seus
                    correspondentes valores individuais.</span><span class="en">The next phase of
                    the project was the description and categorization of each unit of variation. In
                    order to conceptualize the phenomenology of the variation, I elaborated a
                    taxonomy of variants, which I constructed on the basis of the TEI “feature
                    structure” module. A feature structure consists of a set of
                        <em>attribute:value</em> pairs, in which the values can be either atomic or
                    nested feature structures. That is, an element is described through a set of
                    properties that have been assigned a particular value. However, this value can
                    be built, at the same time, by different properties with their corresponding
                    values.</span></p>
            <p><span class="en">The choice of features to define the units of variation was informed
                    by the needs of the project and the type of research I wanted to enable. For
                    example, because my system decomposes higher-level phenomena into low-level
                    atomic features, it is possible to differentiate a linguistic variant from a
                    graphical one. Within the linguistic variants, I then established subcategories
                    until they reached the required level of granularity. This organization of the
                    descriptive markup not only allows me to differentiate morphological and lexical
                    phenomenon from phonetic ones, but it also makes it possible to carry out more
                    complex searches. For example, the encoding of my taxonomy allows for the
                    extraction of those verses in which a phenomenon of variation may have altered
                    the number of syllables, either by reduction or by addition.</span><span
                    class="gl">A escolla dos trazos que definirían cada unha das unidades de
                    variación realizouse atendendo ás necesidades do proxecto e ao tipo de pesquisas
                    que se quería habilitar. Grazas a acumulación de trazos mínimos, pódese, por
                    exemplo, diferenciar unha variante de lingua dunha variante gráfica, e dentro
                    das variantes de lingua é posible establecer subcategorías até chegar ao nivel
                    de granularidade desexado. Esta organización non só facilita que se diferencien
                    os fenómenos de vertente fonética dos morfolóxicos ou lexicais, senón que tamén
                    faculta para que se poidan levar a cabo pesquisas moito máis complexas. Por
                    exemplo, a codificación da nosa taxonomía consinte que se extraian aqueles
                    versos en que un fenómeno de variación poida ter alterado o número de sílabas,
                    tanto por redución como por adición.</span></p>
            <p><span class="gl">Ademais da eficiencia que este sistema habilita á hora de explorar o
                    corpus, o método de codificación deseñado presenta outros beneficios. Por unha
                    parte, a separación do texto primario da súa anotación axuda a garantir a
                    sustentabilidade do proxecto e a súa actualización. Téñase en conta que a
                    información analítica e interpretativa está na vinculación da edición á
                    taxonomía, e non na transcrición propiamente dita. Ademais, é posíbel aumentar o
                    nivel de granularidade, de profundidade con que se describe cada unha das
                    entradas da taxonomía, sen que iso implique facer modificacións na edición. O
                    método de anotación externa fai que a edición sexa facilmente reutilizábel por
                    persoas alleas á súa elaboración inicial. Este factor, xunto co uso dun estándar
                    como TEI, incrementa a sustentabilidade, reciclaxe e reutilización do código
                    empregado. Por outra parte, existen todas as vantaxes que xorden durante o
                    procesamento posterior, tanto na hora da publicación como da
                    análise.</span><span class="en">Beyond the efficiency that this system enables
                    when exploring the corpus, the markup taxonomy that I developed provides other
                    benefits. On the one hand, the separation of the primary text from its
                    annotation enhances the sustainability of the project and facilitates its
                    updating. Specifically, analytical and interpretative information is linked to
                    the edition of the taxonomy, and not embedded in the transcription itself.
                    Furthermore, it is possible to increase the level of granularity with which each
                    of the taxonomy entries is described, and such an enhancement or refinement
                    would not require any changes in the edition itself. As a result, the external
                    (stand-off) annotation method makes the edition easily reusable, for other
                    purposes, by people not involved in its original design and implementation. This
                    factor, together with the use of a standard such as TEI, increases the
                    sustainability, including the recycling and reuse of the data. Additional
                    advantages, discussed below, arise during subsequent processing, involving both
                    publication and analysis. </span></p>
            <p><span class="en">Regarding publication, the taxonomy of variants can be exploited to
                    create the equivalent of a traditional critical apparatus, as well as to
                    translate the information encoded in the taxonomy into visual elements. Each
                    layer of information that forms a taxonomy entry can be retrieved and used to
                    enrich the edition. As can be seen in my digital edition, the presence of
                    certain orthographic or linguistics features is represented through changes in
                    format. In this way, the multiple layers of information that constitute the
                    taxonomy are used typographically to “define” the categorization of the
                    variants; for example, orthographic variants are represented in bold (font
                    weight) and the linguistic ones in indigo (font color). This insertion of visual
                    elements into the edition was implemented by retrieving only one of the features
                    that described the phenomenon (its major type). However, any of the elements
                    that define a variation unit can be used to customize the edition. For instance,
                    if you click on any of the marked variants, a popup dialog presents a specific
                    description of the phenomenon it represents.</span><span class="gl">No que se
                    refire á publicación, a taxonomía de variantes pode ser explorada para presentar
                    equivalentes ao aparato crítico en soporte dixital, así como para a tradución da
                    información da taxonomía en elementos visuais. Cada unha das camadas que
                    conforman unha entrada da taxonomía pode ser accedida e utilizada para
                    enriquecer a edición. Como se pode observar na edición dixital ofrecida, a
                    presenza de determinados trazos é transformada en cambios no formato do texto.
                    Así pois, utilízanse as múltiples capas de información que conforman a taxonomía
                    para materializar tipograficamente a categorización de variantes: por exemplo,
                    as variantes gráficas están representadas en negriña e as lingüísticas en azul
                    anil. Polo tanto, para a inserción de elementos visuais na edición, apenas
                    estamos a utilizar a información dun dos trazos (tipo de taxonomía). Porén,
                    poderiamos servirnos de calquera dos elementos que conforman a definición da
                    variante para customizar a edición. Por exemplo, de clicarse sobre algunha das
                    variantes marcadas, un diálogo emerxente presenta a descrición específica dese
                    fenómeno.</span></p>
            <p><span class="gl">Durante o proceso de codificación da transcrición, decidiuse
                    representar simultaneamente diferentes criterios, combinando varios niveis de
                    intervención editorial: por unha parte, mantense unha transcrición conservadora;
                    pola outra, vanse introducindo camadas de intervencións que camiñan cara a
                    criterios máis regularizadores. Esta dobre codificación tradúcese en ofrecerlle
                    aos lectores a posibilidade de mudar algúns dos criterios de edición á vontade.
                    Por exemplo, pódese escoller con total liberdade como se queren visualizar as
                    abreviaturas, polo que se pode ler a abreviatura ou visualizar o seu
                    desenvolvemento con tan só facer un clic nese criterio. Xunto coas abreviaturas,
                    esta combinación de criterios tamén foi aplicada ás cancelacións, correccións e
                    adicións contemporáneas ao acto de copia e a determinados elementos referidos á
                    separación de palabras. Nalgúns casos, pode verse a distribución presente nos
                    orixinais ou pódese ler unha versión en que se aglutinan e separan determinados
                    elementos segundo conformen unidades léxicas. </span><span class="en">During the
                    process of encoding the transcription, I decided to represent different criteria
                    simultaneously, combining several levels of editorial intervention. As a result,
                    on the one hand, a conservative transcription is preserved. On the other hand,
                    there are interventions that introduce layers that contribute to regularizing
                    criteria. This dual encoding offers the reader the possibility of changing some
                    of the edition criteria at will. For example, the reader controls whether to
                    render an abbreviation mark as it appears in the manuscript or as an editorial
                    expansion of the abbreviation. This flexibility is important because different
                    readers will have different requirements in this respect; for example, a
                    literary scholar may prefer the expansions because they create a text that is
                    easier to read, while a linguist may prefer the abbreviations because they are
                    closer to the material evidence of the witnesses, that is, they incorporate less
                    intervention by the modern editor. This reader-oriented theory of edition is
                    also applied to the cancellations and additions that were contemporary to the
                    copy, and to certain elements pertaining to the separation of words. For
                    example, in some places the reader can choose to see the original distribution
                    of white spaces or read the textual components as they are agglutinated or
                    deglutinated according to their composition in lexical units.</span></p>
            <p><span class="en">The aim of this part of the project is to create a digital
                    workstation that allows the exploration of the textual corpus. Although this
                    workstation was designed to interrogate the texts in a very specific way,
                    dictated by the objectives of our investigation, it is also a resource of
                    interest and utility to a broader scholarly population. This edition can serve
                    as an auxiliary tool for research about the literary corpus, as well as for
                    linguistic studies regarding the Medieval period. Therefore, with this objective
                    in mind, it was important to create an intuitive and friendly interface with
                    correct and adequate documentation.</span><span class="gl">O obxectivo desta
                    parte do proxecto é crear unha estación de traballo dixital que permita a
                    exploración deste corpus textual. Aínda que foi deseñado para interrogar os
                    textos dunha maneira moi concreta dependendo dos obxectivos da nosa
                    investigación, consideramos que se dispoñibilizou un recurso de interese para o
                    público especializado, podendo converterse nunha ferramenta auxiliar á
                    investigación sobre a lírica galego-portuguesa. Así pois, con este obxectivo en
                    mente, era importa deseñar unha interface intuitiva e amigábel, correctamente
                    documentada.</span></p>
